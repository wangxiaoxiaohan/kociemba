struct cube_t{
	int8_t ep[12];
	int8_t cp[8];
	int8_t eo[12];
	int8_t co[8];
	cube_t(){
		for(int8_t i = 0 ; i  < 12 ; i++){
			if(i < 8){
				cp[i] = i;
				co[i] = 0;
			}
			ep[i] = i;
			eo[i] = 0;
		}
	}
};

